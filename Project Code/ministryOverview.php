    <div class="section">
        <h2>OVERVIEW</h2>
        <p>The Ministry of Industries, industry and commerce was established in year 1931 as one of the first Ministries in Sri Lanka. It has the primary responsibility of promoting the industrial sector. According to the Extraordinary Gazette notification No. 2196/27 dated 06.10.2020, the scope of the Ministry is to strengthen and promote the local industrial sector by adopting advanced strategies to earn relatively large profits in the context of global competition.</p>
        <p>This includes establishing links with the local, regional, and international supply networks through diversification of industries.</p>
        <p>The Ministry of Industry directs three state Ministries and 17 various statutory bodies and public corporations. It is responsible for the implementation of the Industrial Development Act No. 46 of 1990 and the National Enterprise Development Authority Act No. 17 of 2006.</p>
    </div>

